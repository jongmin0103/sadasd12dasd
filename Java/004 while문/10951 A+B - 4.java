import java.util.*;
 
public class Main {
	public static void main(String[] args) {

        // 입력하기
        Scanner sc = new Scanner(System.in);
        int a = 0;
        int b = 0;
        
        // 출력하기
        while(sc.hasNextInt())
        {
            a = sc.nextInt();
            b = sc.nextInt();
            
            System.out.println(a+b);
        }
        
        sc.close();
        
	}
}
